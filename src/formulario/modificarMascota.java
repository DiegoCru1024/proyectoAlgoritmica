    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String Dueño = txt_dueño.getText();
        String Mascota = txt_mascota.getText();
        String Raza = txt_raza.getText();
        String Color = txt_color.getText();
        int Edad = Integer.parseInt(txt_edad.getText());
        double Peso = Double.parseDouble(txt_peso.getText());

        mascota.setDueño(Dueño);
        mascota.setNombre(Mascota);
        mascota.setRaza(Raza);
        mascota.setColor(Color);
        mascota.setEdad(Edad);
        mascota.setPeso(Peso);

        mainForm.definirTablaMascotas();
        mainForm.guardarDatos();
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

