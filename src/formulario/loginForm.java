    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String user = txt_user.getText();
        String pass = txt_pass.getText();
        
        JSONObject userObj = null;
        String apiUser = null, apiPass = null;
        boolean apiAdmin = false;
        
        for(Object obj : dataObject){
            JSONObject jsonObj = (JSONObject) obj;
            apiUser = (String) jsonObj.get("username");
            
            if(apiUser.equalsIgnoreCase(user)){
                apiPass = (String) jsonObj.get("password");
                apiAdmin = (Boolean) jsonObj.get("isAdmin");
                userObj = jsonObj;
                break;
            } else {
                apiUser = null;
                apiPass = null;
                apiAdmin = false;
                userObj = null;
            }
        }
        
        if(apiUser != null){
            if(apiUser.equalsIgnoreCase(user) && apiPass.equals(pass)){
                JOptionPane.showMessageDialog(this, "Sesi√≥n iniciada correctamente...");
                if(apiAdmin == true){
                    adminMainForm obj = new adminMainForm(userObj);
                    obj.setVisible(true);
                    this.dispose();
                } else {
                    userMainForm obj = new userMainForm(userObj);
                    obj.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(this, "La contrase√±a es incorrecta...");
            }
        } else {
            JOptionPane.showMessageDialog(this, "El usuario no esta registrado...");
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

