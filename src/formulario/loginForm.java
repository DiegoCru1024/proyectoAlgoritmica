    public void loadCredentials(){               
        try{
            URL loginApiUrl = new URL("https://6371572b07858778617b1464.mockapi.io/deliveryAPI/loginAccess");
            HttpURLConnection connection = (HttpURLConnection) loginApiUrl.openConnection();
            connection.setRequestMethod("GET");
            connection.connect();
            
            //Check if connect is made
            int responseCode = connection.getResponseCode();

            // 200 OK
            if (responseCode != 200) {
                throw new RuntimeException("HttpResponseCode: " + responseCode);
            } else {

                StringBuilder informationString = new StringBuilder();
                Scanner scanner = new Scanner(loginApiUrl.openStream());

                while (scanner.hasNext()) {
                    informationString.append(scanner.nextLine());
                }
                //Close the scanner
                scanner.close();


                
                JSONParser parse = new JSONParser();
                dataObject = (JSONArray) parse.parse(String.valueOf(informationString));
            }
            
        } catch(Exception e) {
            e.printStackTrace();
        }
    }

